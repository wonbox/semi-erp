package org.semierp.vo;

public class productInfo {
	int pno;
	String pname;
	int pnum;
	String pdiv;
	int price;
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPnum() {
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public String getPdiv() {
		return pdiv;
	}
	public void setPdiv(String pdiv) {
		this.pdiv = pdiv;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
