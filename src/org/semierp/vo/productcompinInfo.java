package org.semierp.vo;

public class productcompinInfo {
	int pno;
	String pname;
	int insize;
	String indate;
	int sno;
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getInsize() {
		return insize;
	}
	public void setInsize(int insize) {
		this.insize = insize;
	}
	public String getIndate() {
		return indate;
	}
	public void setIndate(String indate) {
		this.indate = indate;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	
}
