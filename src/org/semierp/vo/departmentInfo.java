package org.semierp.vo;

public class departmentInfo {
	int deno;
	String dename;
	String detel1;
	String detel2;
	int emno;

	public int getDeno() {
		return deno;
	}

	public void setDeno(int deno) {
		this.deno = deno;
	}

	public String getDename() {
		return dename;
	}

	public void setDename(String dename) {
		this.dename = dename;
	}

	public String getDetel1() {
		return detel1;
	}

	public void setDetel1(String detel1) {
		this.detel1 = detel1;
	}

	public String getDetel2() {
		return detel2;
	}

	public void setDetel2(String detel2) {
		this.detel2 = detel2;
	}

	public int getEmno() {
		return emno;
	}

	public void setEmno(int emno) {
		this.emno = emno;
	}

}
